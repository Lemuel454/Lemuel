numero1 = int(input("ingrese primer numero: "))
numero2 = int(input("ingrese segundo numero: "))
if numero1 > numero2:
    print ("el primero numero ",numero1," es mayor al segundo numero ",numero2)
elif numero1 < numero2:
    print ("el primer numero ",numero1," es menor al segundo numero ",numero2)
else:
    print ("ambos numero son iguales")